def sumar (x,y):
    suma = x+y
    return suma

def restar (x,y):
    resta = x-y
    return resta

def dividir (x,y):
    division = x/y
    return division

def multiplicar (x,y):
    multiplicacion = x*y
    return multiplicacion

print (sumar(3,8))
print (restar(11,7))
print (dividir (500,20))
print (multiplicar (5,30))  